andar = 1
while andar <= 20:
    if andar != 13:
        print(f"{andar}º andar")
    andar += 1

#Neste código, usei um loop while para iterar de 1 a 20. 
# Verificando se o andar não é igual a 13 e, se não for, imprime a mensagem do andar. 
# O loop while continua até que o valor de andar seja maior que 20.